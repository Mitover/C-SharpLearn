using System;
class Program
{
    static void Main()
    {
        int a = 10; int b = 5;
        Console.WriteLine(a + "+" + b + "=" + (a + b)); // 10+5 =15   
        Console.WriteLine(a + "-" + b + "=" + (a - b));
        Console.WriteLine(a + "*" + b + "=" + (a * b));
        Console.WriteLine(a + "/" + b + "=" + (a / b));
    }
}