using System;
class Program
{
    static void Main()
    {
        int N = Convert.ToInt32(Console.ReadLine());
        for (int i = 0; i < N; i++)
        {
            Console.Write(":)\\_____/(: ");
        }
        Console.WriteLine();
        for (int i = 0; i < N; i++)
        {
            Console.Write(" {(@)v(@)}  ");
        }
        Console.WriteLine();
        for (int i = 0; i < N; i++)
        {
            Console.Write(" {|~- -~|}  ");
        }
        Console.WriteLine();
        for (int i = 0; i < N; i++)
        {
            Console.Write(" {/^'^'^\\}  ");
        }
        Console.WriteLine();
        for (int i = 0; i < N; i++)
        {
            Console.Write(" ===m-m===  ");
        }
    }
}